export const charToKeyMap = {};

keyboardRows.forEach(row => {
  row.forEach(k => {
    if (k.key !== undefined) {
      charToKeyMap[k.key] = k;
    }
    if (k.shiftKey !== undefined) {
      charToKeyMap[k.shiftKey] = k;
    }
  });
});

// Map special characters that might be inside exercise texts
charToKeyMap["\n"] = { id: "Enter", finger: "right-pinky" };
charToKeyMap["\r"] = { id: "Enter", finger: "right-pinky" };

export function getRequiredKeys(char) {
  const k = charToKeyMap[char];
  if (!k) return [];
  const keys = [k.id];
  
  // Determine if shift key is required
  const isUpper = char !== char.toLowerCase() && char.match(/[A-Z]/);
  const isShiftedSymbol = ["~", "!", "@", "#", "$", "%", "^", "&", "*", "(", ")", "_", "+", "{", "}", "|", ":", "\"", "<", ">", "?"].includes(char);
  
  if (isUpper || isShiftedSymbol) {
    // Left-hand keys require Right Shift, Right-hand keys require Left Shift
    if (k.finger && k.finger.startsWith("left-")) {
      keys.push("ShiftRight");
    } else {
      keys.push("ShiftLeft");
    }
  }
  return keys;
}

export function getFingerName(fingerId) {
  if (!fingerId) return "";
  const parts = fingerId.split("-");
  if (parts.length === 2) {
    const hand = parts[0].charAt(0).toUpperCase() + parts[0].slice(1);
    const finger = parts[1].charAt(0).toUpperCase() + parts[1].slice(1);
    return `${hand} ${finger}`;
  }
  return fingerId.charAt(0).toUpperCase() + fingerId.slice(1);
}
